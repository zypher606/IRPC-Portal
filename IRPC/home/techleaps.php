        <h2>Frequently Asked Questions (FAQs) :</h2><br />
		<h4>Q1. What is Tech Leaps? <br />
A- Tech Leaps is an initiative by IIT Gandhinagar to promote technical culture and the spirit of innovation among its students. It is a platform for motivated students to demonstrate their innovative thinking and compete for funding of Rs. 1 lac to develop their idea.
 <br /><br />
Q2. There are UL, Ricoh challenges annually in the institute, how does Tech Leaps differ from them?<br />
A- UL and Ricoh in their annual challenges, generally give out a defined domain or problem statement annually. Tech Leaps provides a broad domain for your ideas to develop. Tech Leaps has 2 technologies and 2 problem statements. The teams are free to choose any one from the given two technologies and problems to develop something innovative. Additionally, Tech Leaps will be held twice in one academic year i.e. one per semester.<br /><br />
 
Q3. What will be the deliverables for elevator pitching on 24th October, 2015?<br />
A- Each team will be given a five minute slot for presentation to the panel of experts about their idea from one of their chosen technology or problem statement defined for this edition. <br /><br />
 
Q4. What is the maximum team size for registering in Tech Leaps?<br />
A- 3 members and no restricition of branch, program or year of study. Even an individual candidate can participate in Tech Leaps.
 <br /><br />
Q5. What will be the judging criteria for elevator pitching?<br />
A- All these details will be furnsihed to the registered teams/individuals in a secondary announcement after 10th October, 2015 along with the name of panel members. Broadly, the teams will be judged on the impact factor and feasibility of their ideas. The idea should not be an incremental change, it should strive or aim to transform the way people operate in that domain.<br /><br />
 
Q6. Apart from this challenge, will Tech Leaps contribute in any othe manner?<br />
A- IRP Council, the organizing body of Tech Leaps, in collaboration with Academic and Technical council, will supplement this event with talks/seminars on business, product development, innovation, design, technology etc. There are other activities planned as well.
 <br /><br />
Q7. Whom to contact for any queries related to Tech Leaps?<br />
A- You can contact any member of the IRP Council on irpcouncil2015.pvtgroup@iitgn.ac.in or you can contact irp.secy@iitgn.ac.in </h4>
		
